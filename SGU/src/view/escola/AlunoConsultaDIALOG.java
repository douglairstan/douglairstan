    public void ConsultarAlunoNome(){
        try{
            BaseCTR consultar = new BaseCTR();
            ResultSet rs;
            rs=consultar.ConsultarAlunoNome(jTextFieldPesquisa.getText().toUpperCase().replace("Á", "A").replace("á", "A").replace("É", "E").replace("é", "E").replace("Í", "I").replace("í", "I").replace("ã", "A").replace("Ã", "A"));
            DefaultTableModel modelo = (DefaultTableModel) jTableConsultaAluno.getModel();
            modelo.setNumRows(0);
            while (rs.next()){
                Object[] novaLinha = new Object[6];
                novaLinha[0] = rs.getString("rpe_registro");
                novaLinha[1] = rs.getString("pessoa_ci_uf");
                novaLinha[2] = rs.getString("pessoa_ci_numero");
                novaLinha[3] = rs.getString("pessoa_nome");
                novaLinha[4] = rs.getString("pessoa_dn");
                novaLinha[5] = rs.getString("pessoa_sexo");
                modelo.addRow(novaLinha);
            }
        }
        catch (Exception e){
            System.out.println(e.getMessage());    
            }
    }

    public void ConsultarAluno() {
        if (jRadioButtonNome.isSelected() == true){
            ConsultarAlunoNome();
        }
    }
    
   
    
