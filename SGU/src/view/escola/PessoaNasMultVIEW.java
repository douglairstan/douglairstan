    public void MostrarDadosTabelaPessoaNM(){
        int n = 0;
        jTextFieldContador.setText("");
        try{
            BaseCTR consultarCTR = new BaseCTR();
            ResultSet rs=null;
            rs=consultarCTR.ConsultarPessoaNM(jComboBoxNM.getSelectedItem().toString());
            DefaultTableModel modelo = (DefaultTableModel) jTableNM.getModel();
            modelo.setNumRows(0);
            while (rs.next()){
                n++;
                Object[] novaLinha = new Object[5];
                novaLinha[0] = rs.getString("pessoa_id");
                novaLinha[1] = rs.getString("pessoa_nome");
                novaLinha[2] = rs.getString("pessoa_dn");
                novaLinha[3] = rs.getString("pessoa_sexo");
                novaLinha[4] = rs.getString("cargo_nome");
                modelo.addRow(novaLinha);
                jTextFieldContador.setText(""+n);
            }
        }
        catch (Exception e){
            System.out.println(e.getMessage());    
            }
    }
    
