    public void Iniciar(){
        this.setSize(850, 375);
        this.setTitle("Biblioteca - Consultar Editora");
        jRadioButtonEditora.setSelected(true);
    }
    
    public void InserirEditora(){
        try{
            BaseCTR inserirCTR = new BaseCTR();
            String nome = JOptionPane.showInputDialog("Informe a Editora:","");
            if ("".equals(nome)){
                JOptionPane.showMessageDialog(null,"Campo em Branco. ","Informação: ",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                inserirCTR.InserirEditora(nome.toUpperCase());
		JOptionPane.showMessageDialog(null,""+nome+" Inserido com Sucesso. ","Informação: ",JOptionPane.INFORMATION_MESSAGE);
                jTextFieldPesquisa.setText(nome);
                ConsultarEditora();
            }
	}
            catch(Exception e){
		System.out.println(e.getMessage());
	}
    }
    
    public void ChaveSelecao(){
        int linha = jTableConsultarEditora.getSelectedRow();
        setEditora(jTableConsultarEditora.getValueAt(linha,1).toString());
        this.dispose();
    }
    
    public void Consultar() {
        if (jRadioButtonEditora.isSelected() == true){
            ConsultarEditora();
        }
    }
    
    public void ConsultarEditora(){
        try{
            BaseCTR consultar = new BaseCTR();
            ResultSet rs=null;
            rs=consultar.ConsultarEditora(jTextFieldPesquisa.getText().toUpperCase());
            DefaultTableModel modelo = (DefaultTableModel) jTableConsultarEditora.getModel();
            modelo.setNumRows(0);
            while (rs.next()){
                Object[] novaLinha = new Object[2];
                novaLinha[0] = rs.getInt("acervo_editora_id");
                novaLinha[1] = rs.getString("acervo_editora_nome");
                modelo.addRow(novaLinha);
            }
        }
        catch (Exception e){
            System.out.println(e.getMessage());    
            }
    }
    
    public void Sair(){
        this.dispose();
    }
    
